package com.hotent.makshi.model.director;

public class ProjectNoticebook {
	private String id;
	private String applyer;
	private String applyerID;
	private String department;
	private String departmentID;
	private String apply_at;
	private String director_name;
	private String director_nameID;
	private String project_name;
	private String build_name;
	private String auto_num;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getApplyer() {
		return applyer;
	}

	public void setApplyer(String applyer) {
		this.applyer = applyer;
	}

	public String getApplyerID() {
		return applyerID;
	}

	public void setApplyerID(String applyerID) {
		this.applyerID = applyerID;
	}

	public String getDepartment() {
		return department;
	}

	public void setDepartment(String department) {
		this.department = department;
	}

	public String getDepartmentID() {
		return departmentID;
	}

	public void setDepartmentID(String departmentID) {
		this.departmentID = departmentID;
	}

	public String getApply_at() {
		return apply_at;
	}

	public void setApply_at(String apply_at) {
		this.apply_at = apply_at;
	}

	public String getDirector_name() {
		return director_name;
	}

	public void setDirector_name(String director_name) {
		this.director_name = director_name;
	}

	public String getDirector_nameID() {
		return director_nameID;
	}

	public void setDirector_nameID(String director_nameID) {
		this.director_nameID = director_nameID;
	}

	public String getProject_name() {
		return project_name;
	}

	public void setProject_name(String project_name) {
		this.project_name = project_name;
	}

	public String getBuild_name() {
		return build_name;
	}

	public void setBuild_name(String build_name) {
		this.build_name = build_name;
	}

	public String getAuto_num() {
		return auto_num;
	}

	public void setAuto_num(String auto_num) {
		this.auto_num = auto_num;
	}

}
